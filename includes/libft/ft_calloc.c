#include "libft.h"

void	*ft_calloc(size_t nitems, size_t size)
{
	void	*tab;

	tab = (void *)malloc(nitems * size);
	if (!tab)
		return (NULL);
	ft_memset(tab, 0, nitems * size);
	return (tab);
}
